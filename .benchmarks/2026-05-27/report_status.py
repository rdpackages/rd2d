from __future__ import annotations

import json
from pathlib import Path

import pandas as pd


BENCH_DIR = Path(__file__).resolve().parent
RESULT_DIR = BENCH_DIR / "results"
PROFILE_DIR = BENCH_DIR / "profiles"


def read_csv(path: Path) -> pd.DataFrame:
    if not path.exists():
        return pd.DataFrame()
    return pd.read_csv(path)


def finite_float(value) -> float | None:
    try:
        out = float(value)
    except (TypeError, ValueError):
        return None
    if pd.isna(out):
        return None
    return out


def comparison_records(summary: pd.DataFrame, comparison: str) -> list[dict]:
    if summary.empty:
        return []
    rows = summary[summary["comparison"] == comparison].copy()
    out = []
    for _, row in rows.iterrows():
        out.append(
            {
                "lhs": row["lhs"],
                "rhs": row["rhs"],
                "n_common": int(row["n_common"]),
                "max_abs_diff": finite_float(row["max_abs_diff"]),
                "max_rel_diff": finite_float(row["max_rel_diff"]),
                "worst_case": row["worst_case"],
                "worst_output": row["worst_output"],
                "worst_row": str(row["worst_row"]),
                "worst_column": str(row["worst_column"]),
            }
        )
    return out


def profile_records() -> list[dict]:
    rows: list[dict] = []
    for path in sorted(PROFILE_DIR.glob("*_profile_timings.csv")):
        df = read_csv(path)
        if df.empty or "elapsed_seconds" not in df:
            continue
        stem = path.stem.replace("_profile_timings", "")
        if stem == "r":
            platform = "R"
            fitmethod = "local"
        else:
            parts = stem.split("_", 1)
            platform = parts[0].title()
            fitmethod = parts[1] if len(parts) > 1 else "local"
        medians = df.groupby("case", as_index=False)["elapsed_seconds"].median()
        for _, row in medians.iterrows():
            rows.append(
                {
                    "platform": platform,
                    "fitmethod": fitmethod,
                    "case": row["case"],
                    "median_seconds": finite_float(row["elapsed_seconds"]),
                }
            )
    return rows


def timing_ratio_records(ratios: pd.DataFrame) -> list[dict]:
    if ratios.empty:
        return []
    grouped = (
        ratios.groupby(["platform", "source"], as_index=False)
        .agg(
            cases=("case", "count"),
            median_local_over_public=("local_over_public", "median"),
            median_public_over_local=("speedup_public_over_local", "median"),
        )
        .sort_values(["platform", "source"])
    )
    return [
        {
            "platform": row["platform"],
            "source": row["source"],
            "cases": int(row["cases"]),
            "median_local_over_public": finite_float(row["median_local_over_public"]),
            "median_public_over_local": finite_float(row["median_public_over_local"]),
        }
        for _, row in grouped.iterrows()
    ]


def markdown_table(rows: list[dict], columns: list[str]) -> list[str]:
    if not rows:
        return ["_No data available._"]
    lines = [
        "| " + " | ".join(columns) + " |",
        "| " + " | ".join(["---"] * len(columns)) + " |",
    ]
    for row in rows:
        vals = []
        for col in columns:
            value = row.get(col, "")
            if isinstance(value, float):
                vals.append(f"{value:.3g}")
            else:
                vals.append(str(value))
        lines.append("| " + " | ".join(vals) + " |")
    return lines


def main() -> int:
    RESULT_DIR.mkdir(parents=True, exist_ok=True)
    summary = read_csv(RESULT_DIR / "comparison_summary.csv")
    noncluster = read_csv(RESULT_DIR / "comparison_summary_noncluster.csv")
    ratios = read_csv(RESULT_DIR / "timing_speed_ratios.csv")

    status = {
        "local_separate_cross_platform": comparison_records(summary, "cross_platform_local_separate"),
        "noncluster_public_vs_local_separate": comparison_records(noncluster, "version_public_vs_local_separate"),
        "timing_ratios": timing_ratio_records(ratios),
        "profiles": profile_records(),
    }

    json_path = RESULT_DIR / "status_report.json"
    md_path = RESULT_DIR / "status_report.md"
    json_path.write_text(json.dumps(status, indent=2), encoding="utf-8")

    lines = [
        "# rd2d Benchmark Status",
        "",
        "Generated by `report_status.py` from the current benchmark CSV outputs.",
        "",
        "## Local Separate Cross-Platform",
        *markdown_table(
            status["local_separate_cross_platform"],
            ["lhs", "rhs", "n_common", "max_abs_diff", "max_rel_diff", "worst_case", "worst_output"],
        ),
        "",
        "## Non-Cluster Public vs Local Separate",
        *markdown_table(
            status["noncluster_public_vs_local_separate"],
            ["lhs", "rhs", "n_common", "max_abs_diff", "max_rel_diff", "worst_case", "worst_output"],
        ),
        "",
        "## Timing Ratios",
        *markdown_table(
            status["timing_ratios"],
            ["platform", "source", "cases", "median_local_over_public", "median_public_over_local"],
        ),
        "",
        "## Larger Profile Medians",
        *markdown_table(
            status["profiles"],
            ["platform", "fitmethod", "case", "median_seconds"],
        ),
        "",
    ]
    md_path.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {md_path}")
    print(f"Wrote {json_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
